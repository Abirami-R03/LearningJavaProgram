package ex_05_Switch_Cond;

public class Interview_Question2 {
    public static void main(String[] args) {
        char ch = 'B';
        switch (ch)
        {
            case 66:
                System.out.println("Match ASCII Value");
                break;
            default:
                System.out.println("ASCII Value not Matched");
        }
    }
}
