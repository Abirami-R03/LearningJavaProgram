package ex_05_Switch_Cond;

public class Interview_Question7 {
    public static void main(String[] args) {
        char ch = 'V';
        switch (ch)
        {
            default:
                System.out.println("Hellooooooo");
            case 'V':
                System.out.println("86");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
