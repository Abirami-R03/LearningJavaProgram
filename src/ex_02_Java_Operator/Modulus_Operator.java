package ex_02_Java_Operator;

public class Modulus_Operator {
    public static void main(String[] args) {
        int a=10;
        int b=7;
        System.out.println(a%b);
        //10%6 --> remainder --->4,quotient --->1
        //10%7 --> remainder --->3,quotient --->1
        //any number---> number%2 ----> remainder of even is 0, remainder of odd is 1
    }
}
