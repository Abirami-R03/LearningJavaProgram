package ex_02_Java_Operator;

public class Interview_Char_INT {
    public static void main(String[] args) {
        char a = 'A';//ASCII Value A=65
        //char b = "B";char double quote not support only single quote support
        char b = 'B';//ASCII Value B=66
        System.out.println(a);//A
        System.out.println(a+b);//65+66=131
        //Arithmetic Operation convert char to ASCII int value
    }
}
/*Output
A
131
 */
