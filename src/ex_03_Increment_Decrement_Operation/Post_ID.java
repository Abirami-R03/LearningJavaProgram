package ex_03_Increment_Decrement_Operation;

public class Post_ID {
    public static void main(String[] args) {
        int a =10;
        ++a;//11
        System.out.println(a);
        a++;//12
        System.out.println(a);
        System.out.println(++a);//Increment by 1 and Print--13
        System.out.println(a++);//Print and Increment by 1--13


    }
}
