package ex_01_Java_Basics;

public class Int_Literals {
    public static void main(String[] args) {
        int age = 65;
        //literal - 65, literal_Datatype = int, variable_name = age
        System.out.println(age);

        final int girls_age = 18;
        //literal - 18
        //final value never change
        System.out.println(girls_age);

        int Enum =9;//enum is keyword,Enum we can use
        //literal - 9, literal_Datatype = int, variable_name = Enum
        System.out.println(Enum);





    }
}
